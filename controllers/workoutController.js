"use strict";
const service = require("../services/workoutServices");

//Methods for each endpoint (controller).
const getAllWorkouts = (req, res) => {
  const { mode } = req.query;
  try {
    const allWorkouts = service.getAllWorkoutsService({ mode });
    res.status(201).send({ status: "ok", data: allWorkouts });
  } catch (error) {
    res
      .status(error?.status || 500)
      .send({ status: "FAILED", data: { error: error?.message || error } });
  }
};

const postWorkout = (req, res) => {
  const { body } = req;
  if (
    !body.name ||
    !body.mode ||
    !body.equipment ||
    !body.exercises ||
    !body.trainerTips
  ) {
    res.status(400).send({
      status: "Failed",
      data: {
        error:
          "One of the following keys is missing or is empty in request body: 'name', 'mode', 'equipment', 'exercises', 'trainerTips'",
      },
    });
    return;
  }

  //storing data parsed into json format.
  const newWorkout = {
    name: body.name,
    mode: body.mode,
    equipment: body.equipment,
    trainerTips: body.trainerTips,
  };
  try {
    const createdWorkout = service.postWorkoutService(newWorkout);
    res.status(201).send({ status: "ok", data: createdWorkout });
  } catch (error) {
    res
      .status(error?.status || 500)
      .send({ status: "FAILED", data: { error: error?.message || error } });
  }
};

const getOneWorkout = (req, res) => {
  const {
    params: { workoutId },
  } = req;

  if (!workoutId) {
    res.status(400).send({
      status: "FAILED",
      data: { error: "Parameter ':workoutId' can not be empty" },
    });
  }
  try {
    const oneWorkout = service.getOneWorkoutService(workoutId);
    res.send({ status: "ok", data: oneWorkout });
  } catch (error) {
    res
      .status(error?.status || 500)
      .send({ status: "FAILED", data: { error: error?.message || error } });
  }
};

const updateOneWorkout = (req, res) => {
  const {
    body,
    params: { workoutId },
  } = req;

  if (!workoutId) {
    res.status(400).send({
      status: "FAILED",
      data: { error: "Parameter ':workoutId' can not be empty" },
    });
  }

  try {
    const updatedWorkout = service.updateOneWorkoutService(workoutId, body);
    res.send({ status: "ok", data: updatedWorkout });
  } catch (error) {
    res
      .status(error?.status || 500)
      .send({ status: "FAILED", data: { error: error?.message || error } });
  }
};

const deleteOneWorkout = (req, res) => {
  const {
    params: { workoutId },
  } = req;

  if (!workoutId) {
    res.status(400).send({
      status: "FAILED",
      data: { error: "Parameter ':workoutId' can not be empty" },
    });
  }
  try {
    service.deleteOneWorkoutService(workoutId);
    res.status(201).send({ status: "ok" });
  } catch (error) {
    res
      .status(error?.status || 500)
      .send({ status: "FAILED", data: { error: error?.message || error } });
  }
};

module.exports = {
  getAllWorkouts,
  postWorkout,
  getOneWorkout,
  updateOneWorkout,
  deleteOneWorkout,
};
